; DEBUG PART OF SMALL-C
; PRINTS THE ENTERED FUNCTION NAME
;
 LIB CCEQU.SYS
 NAME DEBUG
 GLOBAL DEBUG
DEBUG LDA #13 OUTPUT CR/LF
 JSR PUTCHR
 LDA #10
 JSR PUTCHR
 PULS X GET ADR
LOOP LDA ,X+ GET CHARACTER
 CMPA #0 LAST ONE?
 BNE D1 NO, CONTINUE
 JMP ,X ELSE RETURN
D1 JSR PUTCHR
 BRA LOOP
 END 
